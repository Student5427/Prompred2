    <p class="main">
    <img border="0" src="/support/images/m4.gif" width="19" height="13" align="absmiddle"><span class="dt2"><b>Заседания Правления СПП (р) РК с Главой республики Катанандовым С.Л. в октябре</b></span><br>

    

    <!--текст странички-->
    </p><p>26 октября Глава республики Сергей Катанандов провел рабочую встречу с представителями Союза промышленников и предпринимателей Карелии. Во встрече приняли участие председатель республиканского союза Юрий Пономарев, президент Торгово-промышленной палаты Республики Карелия Александр Панкратов, министр экономического развития РК Михаил Юринов, члены правления республиканского союза и руководители отраслевых союзов предпринимателей, представители предприятий крупного, среднего и малого бизнеса.</p>
<p>Такие встречи уже стали традиционными. В ходе диалога между властью и бизнесом выявляются наиболее сложные проблемы, с которыми на сегодняшний день приходится сталкиваться предпринимателям Карелии, и пути их решения. Многие из названных в этот раз проблем обусловлены экономическим кризисом, другую часть вполне можно и нужно решать на региональном, а если потребуется, то и на федеральном уровне.</p>
<p><br>
&nbsp;</p>
<p>В их числе уже звучавшие раньше постоянный рост тарифов на энергоносители, отсутствие альтернативы на рынке электроэнергии и многочисленные проверки со стороны федеральных контролирующих органов и новые, чаще всего связанные с применением вполне конкретных федеральных и карельских законов. Случается и так, что вполне правильный на бумаге закон встречает на своем пути препятствия, требующие внесения дополнительных изменений в остальное законодательство. Говорили предприниматели и о трудностях, с которыми приходится сталкиваться при участии в конкурсах и аукционах на получение тех или иных заказов. Еще одна недавно появившаяся проблема – высокие пошлины на ввоз техники из-за рубежа для лесных предприятий, когда приобретение новых тракторов становится нерентабельным для многих леспромхозов. Была названа и общая практически для всех предприятий задача – необходимость объединять усилия при продвижении интересов карельского бизнеса на межрегиональном уровне.</p>
<p>Глава республики предложил предпринимателям совместно поработать над каждой из названных проблем. С другой стороны, отметил Сергей Катанандов, отраслевые союзы в последнее время несколько ослабили работу, все реже выходят в Правительство и федеральные органы с конкретными предложениями по изменению законодательства. «Сегодня бизнес вести сложно, но мы всегда проводили линию на поддержку карельских предприятий. Если необходимо внести изменения в республиканские законы, то сделать это можно быстро и оперативно, мы ждем ваших предложений, - сказал Сергей Катанандов. – Что же касается федерального законодательства, то необходимо входить с реальными поправками в федеральные законы, не стесняться лоббировать интересы северян, а, если надо, то и объединяться с соседними регионами и Российским союзом промышленников и предпринимателей для решения вполне конкретных проблем». Глава Карелии предложил также активнее обсуждать возникающие проблемы на заседаниях антикризисной комиссии Правительства и коллегиях отраслевых министерств.</p>
