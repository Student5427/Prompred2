<b><a href="link311" class="nl">Собрание Союза промышленников и предпринимателей (работодателей) Республики Карелия</a></b>
<br>22 декабря 2010 года в 15 часов 00 минут в Зале заседаний Правительства РК по адресу: г. Петрозаводск, пр. Ленина, д. 19, состоялось собрание Регионального объединения работодателей Республики Карелия «Союз промышленников и предпринимателей (работодателей) Республики Карелия», с повесткой дня «О концепции социально-экономического развития Республики Карелия и задачах бизнес-сообщества Карелии на 2011 год».

<br><br>
</p>
    
<p class="main">

<b>Собрание Регионального  объединения работодателей Республики Карелия «Союз промышленников  и предпринимателей (работодателей) Республики Карелия»</b>
<br>08 ноября 2010 года в 16 часов 00 минут в зале заседания (3-й этаж) ОАО ЛХК «Кареллеспром» по адресу: г. Петрозаводск, ул. Андропова, д. 2. состоится собрание Регионального объединения работодателей Республики Карелия «Союз промышленников и предпринимателей (работодателей) Республики Карелия».
Повестка дня:<br>
1. О концепции социально-экономического развития Республики Карелия.
Докладчик: Представитель Правительства Республики Карелия.
2. Организационный вопрос.

<br><br>
</p>
    
<p class="main">

<b>Заседание Бюро Правления СПП (р) РК в августе 2010 г.</b>
<br>В соответствии с планом работы регионального отделения Союза промышленников и предпринимателей (работодателей) Республики Карелия 30 августа 2010 года в 16 часов по адресу: г. Петрозаводск, ул. Андропова, д. 2, состоялось заседание Правления СПП (р) РК с повесткой дня:
1. О реализации мер по сохранению имеющихся и созданию новых рабочих мест, предотвращению массовых увольнений, переобучению и повышению квалификации высвобождаемых работников;<br>
2. Об установлении в Республике Карелия новой величины минимальной заработной платы.<br>
3. О приеме в члены СПП (р) РК ООО «МПК-Гарант».

<br><br>
</p>
    
<p class="main">

<b><a href="link312" class="nl">Заседание Бюро Правления СПП (р) РК в мае 2010 г.</a></b>
<br>31 мая 2010 года в 16 часов по адресу: г. Петрозаводск, ул. Андропова, д. 2, зал заседания (3-й этаж) состоялось заседание Бюро Правления СПП (р) РК с повесткой дня:<br>
1. Об изменении перспективного плана работы Союза промышленников и предпринимателей (работодателей) Республики Карелия на 2010 год в связи с решением XVII Съезда Общероссийской общественной организации и III Съезда Общероссийского объединения работодателей РСПП.<br>
Докладчик – Президент СПП (р) РК Ю.И. Пономарев.<br>
2. Формирование эффективной и стабильной системы тарифо - и ценообразования на энергетическом рынке Карелии, взаимоувязанной с мерами по развитию конкуренции.<br>
Докладчики:<br>
- Азаров Юрий Петрович – генеральный директор ОАО «Энергокомфорт».<br>
- Доценко Олег Михайлович – генеральный директор ОАО «Карельская энергосбытовая компания».<br>
- Тельнов Олег Владимирович – Председатель Государственного комитета по энергетике и регулированию тарифов.

<br><br>
</p>
    
