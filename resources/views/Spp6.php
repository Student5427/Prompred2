    <p class="main">
    
    <b>Информация для работодателей</b>
    <br>Закон Республики Карелия от 27.12.2004 г. № 841-ЗРК «О квотировании рабочих мест для трудоустройства инвалидов» устанавливает организациям, расположенным на территории Республики Карелия, численность работающих в которых составляет более 100 человек, квоту для приема на работу инвалидов в размере 3% к среднесписочной численности работников (статья 2). Квота – это минимальное количество рабочих мест для трудоустройства инвалидов, которые организация обязана создать или выделить, включая количество рабочих мест, на которых уже работают инвалиды.<br>
    В соответствии со статьей 4 данного Закона, организации, согласно установленным квотам, выделяют рабочие места по профессиям, соответствующим возможностям трудоустройства инвалидов, или создают для них новые рабочие места (в том числе специальные).<br>
    В целях реализации требований Закона РК, Министерством труда и занятости РК издан приказ от 20.09.2010 г. №143-П «Об утверждении минимального количества специальных рабочих мест для трудоустройства инвалидов», которым устанавливается минимальное количество специальных рабочих мест в пределах установленной квоты для организаций, осуществляющих деятельность на территории Республики Карелия, со среднесписочной численностью:<br>
    от 101 до 500 чел. – одно специальное рабочее место;<br>
    от 501 до 1000 чел. – два специальных рабочих места;<br>
    от 1001 до 5000 чел. – три специальных рабочих места;<br>
    более 5001 чел. – четыре специальных рабочих места.<br>
    Таким образом, предприятие, среднесписочная численность работников которого составляет 101 человек, выполнит Закон РК в том случае, если создаст три рабочих места для инвалидов, в том числе одно специальное.<br>
    Выполнением квоты считается трудоустройство инвалидов, подтвержденное заключением трудовых договоров, период работы по каждому из которых в течение календарного года составил не менее трех месяцев. <br>
    Служба занятости готова предоставить помощь работодателям не только в подборе граждан с установленной группой инвалидности для выполнения установленной законом квоты, но и оказать материальную поддержку. Согласно Региональной программы поддержки занятости населения в РК на 2011 года, из федерального и республиканского бюджетов будут выделяться средства на возмещение работодателям затрат на приобретение, монтаж и установку оборудования для оснащения специального рабочего места для трудоустройства незанятого инвалида, в сумме, не превышающей 50 тысяч рублей за одно рабочее место. <br>
    Заинтересованных работодателей приглашаем обращаться в ГУ «Центр занятости населения г.Петрозаводска», ул.Свердлова, 17 каб.8. Тел.59-26-14.
    
