    <b>Уважаемые коллеги,</b>
    <br>В целях содействия участникам внешнеэкономической деятельности Министерство экономического развития РФ в 2010 году запустило информационно-поисковую систему «Портал внешнеэкономической информации»: http://www.ved.gov.ru. <br>
    Портал является общегосударственным информационным ресурсом и аккумулирует сведения об условиях доступа на рынки иностранных государств, о проводимых тендерах за рубежом, инструментах поддержки российского экспорта, интеграционных процессах, проходящих с участием России, а также справочная и статистическая информация. Кроме того, на портале созданы интерактивные ресурсы, позволяющие экспортеру обратиться с вопросами к экспертам профильных органов государственной власти, ответственных за выработку внешнеэкономической политики, а также к торговым представителям в зарубежных странах. Портал, таким образом, является важным информационным «окном», позволяющим компании выстроить свою внешнеэкономическую стратегию.<br>
    В случае заинтересованности в расширении информационно-аналитического наполнения портала свои предложения можно направлять по эл.адресу: ved@economy.gov.ru
    
