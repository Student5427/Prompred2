    <p class="main">
    
    <b>Уважаемые работодатели!</b>
    <br>4 июля 2011 года Правительство Республики Карелия, ОО «Объединение организаций профсоюзов в Республике Карелия» и Союз промышленников и предпринимателей (работодателей) Республики Карелия заключили Соглашение о минимальной заработной плате в Республике Карелия, которое прошло уведомительную регистрацию в Министерстве труда и занятости Республики Карелия (регистрационный номер № 3 от 4 июля 2011 года), размещено на официальных сайтах Правительства Республики Карелия, Министерства труда и занятости Республики Карелия, ОО «Объединение организаций профсоюзов в Республике Карелия», Союза промышленников и предпринимателей (работодателей) Республики Карелия.<br>
    Соглашение о минимальной заработной плате в Республике Карелия устанавливает размер минимальной заработной платы для работников, состоящих в трудовых отношениях с работодателями (физическими и юридическими лицами, осуществляющими деятельность на территории Республики Карелия). <br>
    Руководствуясь статьей 133.1 Трудового кодекса Российской Федерации предлагаю работодателям, осуществляющим деятельность на территории Республики Карелия и не участвовавшим в заключении указанного Соглашения, присоединиться к нему.<br>
    Если в течение 30 календарных дней со дня официального опубликования настоящего предложения работодателями не будет представлен в Министерство труда и занятости Республики Карелия ( 185005, г.Петрозаводск, пр.А.Невского, д.33) мотивированный письменный отказ от присоединения, то указанное Соглашение считается распространенным на этих работодателей со дня официального опубликования настоящего предложения и подлежит обязательному исполнению ими (опубликовано в газете "Карелия" № 47 от 07.07.2011 г.).<br>
    К указанному отказу должны быть приложены протокол консультаций работодателя с выборным органом первичной профсоюзной организации, объединяющей работников данного работодателя, и предложения по срокам повышения минимальной заработной платы работников до размера, предусмотренного Соглашением о минимальной заработной плате в Республике Карелия.
