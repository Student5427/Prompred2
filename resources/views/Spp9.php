<b>Комиссия по разработке антикризисных мероприятий</b>
<br>Первое заседание республиканской комиссии по обеспечению устойчивости развития экономики региона и разработке антикризисных мероприятий состоялось 24 декабря 2008 года, в состав которой входит Президент СПП (р) РК Юрий Пономарев.&nbsp;
На заседании комиссии Премьер-министр Правительства Карелии Павел Чернов заявил о том, что крупнейшие градообразующие предприятия Карелии могут расчитывать на поддержку федерального центра.
Глава Карелии Сергей Катанандов добился вынесения вопроса о поддержке ведущих карельских производителей на рассмотрение всероссийской антикризисной комиссии. Это позволит предприятиям, включенным в федеральный список, получать крупные кредиты, компенсации процентных ставок.
Юрий Пономарев выступил с предложением внести в план мероприятий проведение финансового анализа на социально значимых предприятиях с целью выявления предпосылок банкротства предприятий.

